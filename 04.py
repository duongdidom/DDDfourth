# string and number are always not equal to each other
print ( 42 == '42')

# however, float and integer can be equal to each other
print ( 42.0 == 42 )

# and & or
True and True   # = true
True and False  # = false
False and False # = false
True or True    # = true
True or False   # = true
False or False  # = false